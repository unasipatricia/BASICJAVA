public class Ifelseif {
    public static void main(String[] args) {
        String city = "Delhi";
        if(city == "Meerut") {
            System.out.println("city is Meerut");
        }else if (city == "Nairobi") {
            System.out.println("city is Nairobi");
        }else if(city == "Acra") {
            System.out.println("city is Acra");
        }else {
            System.out.println(city);
        }
    }
}
